char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0 && str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		else if ((str[i] <= 47) || (str[i] >= 58 && str[i] <= 64)
			|| (str[i] >= 91 && str[i] <= 96) || (str[i] >= 123))
		{
			if (str[i + 1] >= 'a' && str[i + 1] <= 'z')
			{
				str[i + 1] -= 32;
				i++;
			}
			else if (str[i + 1] >= 'A' && str[i + 1] <= 'Z')
				i++;
		}
		else if (str[i] >= 'A' && str[i] <= 'Z' && i != 0)
			str[i] += 32;
		i++;
	}
	return (str);
}
